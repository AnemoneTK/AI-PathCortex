                if isinstance(lang, dict) and 'name' in lang:
                    proficiency = lang.get('proficiency', 0)
                    prog_langs.append(f"{lang['name']} (‡∏£‡∏∞‡∏î‡∏±‡∏ö {proficiency}/5)")
                elif isinstance(lang, str):
                    prog_langs.append(lang)
        
